package com.diholapp.android.shaking;

public enum ShakingCodes {

    /*
     * Location permission has not been accepted.
     */
    LOCATION_PERMISSION_ERROR,

    /*
     * Location is disabled.
     */
    LOCATION_DISABLED,

    /*
     * Invalid API key.
     */
    AUTHENTICATION_ERROR,

    /*
     * Expired API key.
     */
    API_KEY_EXPIRED,

    /*
     * Server is not available.
     */
    SERVER_ERROR,

    /*
     * Device sensors are not available.
     */
    SENSOR_ERROR,

    /*
     * Audio permission has not been accepted.
     */
    AUDIO_PERMISSION_ERROR,

    /*
     * Audio permission has not been accepted.
     */
    AUDIO_ERROR
}
